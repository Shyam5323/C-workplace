#include <iostream>
using namespace std;
int main(){
    //Addition
    int number1{2};
    int number2{7};

    int result = number1+number2;
    cout<<"Result: "<<result<<endl;
    //Subtracton
    result = number2 - number1;
    cout<<result<<endl;

    //Multiply
    result= number1*number2;
    cout<<result<<endl;

    //Division
    result= number2/number1;
    cout<<result<<endl;
    return 0;

    //Modulus (%) - remainder

}